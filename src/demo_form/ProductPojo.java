package demo_form;

public class ProductPojo {
	
	private int  imgid;
	private String name;
	private String desc;
	private int price;
	private int  hour;
	private int incr;
	public int getImgid() {
		return imgid;
	}
	public void setImgid(int imgid) {
		this.imgid = imgid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	}
	public int getIncr() {
		return incr;
	}
	public void setIncr(int incr) {
		this.incr = incr;
	}
	
	
	
	
}
